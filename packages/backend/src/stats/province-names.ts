/**
 * 省名。市级码会先归一到省级（前两位 + "0000"）再查表——聚合可能落到市码，
 * 但省份分布只按省展示。查不到时回退原码，至少可读。
 */
export function provinceName(code: string): string {
  const provinceCode = `${code.slice(0, 2)}0000`
  return PROVINCE_NAMES[provinceCode] ?? PROVINCE_NAMES[code] ?? code
}
